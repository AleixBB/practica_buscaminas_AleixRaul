package tqs.prac.model;

public class Cell {
    private int value;
    public Cell()
    {
        value = 0;
    }
    public Cell(int value)
    {
        this.value = value;
    }
    public int getValue()
    {
        return value;
    }
    public void setValue(int val)
    {
        value = val;
    }

    
}
